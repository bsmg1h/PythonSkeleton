class MathDemo:

    @staticmethod
    def add(a, b):
        return a+b

    @staticmethod
    def minus(a, b):
        return a-b

    @staticmethod
    def multi(a, b):
        return a*b

    @staticmethod
    def divide(a, b):
        return a/b


